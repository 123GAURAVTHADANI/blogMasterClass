var { Schema, default: mongoose } = require("mongoose");
const blogSchema = new Schema(
  {
    title: {
      type: String,
      required: true,
    },
    description: {
      type: String,
      required: true,
    },
    user: {
      type: Schema.Types.ObjectId,
      ref: "User",
    },
  },
  { timestamps: true }
);

var Article = mongoose.model("article", blogSchema);
module.exports = { Article };
